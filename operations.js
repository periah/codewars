function basicOp(operation, value1, value2)
{
  let add = value1 + value2
  let subtract = value1-value2
  let multiply = value1*value2
  let divide = value1/value2
  if (operation === '+'){
      return add
      }
  else if (operation === '-'){
    return subtract
  }else if (operation === '*'){
            return multiply
  }else if (operation === '/'){
            return divide
            }

  // Code
}
